package basics;

public class Operator {
	public static void main(String... args) {
		System.out.println(1 + 1);
		System.out.println(1 - 1);
		System.out.println(2 * 2);
		System.out.println(2 / 2);
		System.out.println(2 % 2);
		int n = 9;
		System.out.println(n++);
		System.out.println(n--);
		System.out.println(8 == 9);
		System.out.println(2 & 2);
		System.out.println(2 | 2);

	}
}
